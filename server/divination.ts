/**
 * 深層鑑定文生成ロジック
 * 学習データの文体を完全に再現し、顧客個別の悩みに深く対応する
 */

import { callClaudeSonnet } from './openrouter';
import { getAllTrainingData } from './db';

/**
 * プロンプトテンプレート（学習データの文体を完全に再現）
 */
const DIVINATION_PROMPT_TEMPLATE = `あなたはプロの霊視鑑定師です。顧客のチャット履歴を深く読み取り、魂に響く深層鑑定【破】を作成してください。

<絶対に守るべきルール>
1. **文字数の厳守**: 合計4,000～5,000文字で書いてください。**5,000字を絶対に超えないでください。**各章は簡潔にまとめ、全体のバランスを取ってください。質の高い内容を凝縮し、冗長な繰り返しや過度な詳細描写は避けてください。
2. **構成**: 以下の構成を必ず守ってください
   - タイトル: # {顧客名}さん 深層鑑定【破】
   - 導入部（200字程度）: 顧客の言葉を引用し、深く共感する。
   - 第一章（400字程度）: 魂が抱えている三つの傷を簡潔に描写。
   - 第二章（350字程度）: 顧客の直感が正しいことを断言。
   - 第三章（500字程度）: 前世の物語を創作。
   - 第四章（600字程度）: 半年後、1年後、3年後の未来を描写。
   - 第五章（400字程度）: 三つの道を簡潔に説明。
   - 結び（200字程度）: 魂からの最後のメッセージ。
3. **文体の特徴を完全に再現してください**:
   - 非常に感情的で深く共感する語りかけ（「辛かったですよね」「よく頑張ってきましたね」「苦しかったですよね」）
   - スピリチュアルな表現を多用（「視えました」「波動が伝わってきました」「魂が叫んでいます」）
   - 顧客の内面を深く推測して具体的に描写（「泣きたかった日も、諦めかけた夜も」）
   - 繰り返しの強調（「本当に本当に」「深く深く」「ずっと、ずっと」）
   - 問いかけと確認（「〜ですよね？」「当てはまりますか？」）
   - 断定的な表現（「断言します」「間違いありません」「視えています」）
4. **顧客個別対応（最優先）**:
   - 顧客の年齢、家族構成、具体的な悩みを必ず反映
   - チャット履歴から読み取れる情報を最大限活用
   - 一般論ではなく、この顧客にしか当てはまらない内容を書く
</絶対に守るべきルール>

<文体の参考例>
以下は実際の深層鑑定文の一部です。この文体・トーン・雰囲気を完全に再現してください（内容はコピーせず、文体のみ参考にしてください）:

{trainingExample}
</文体の参考例>

<顧客とのチャット履歴>
{customerChatHistory}
</顧客とのチャット履歴>

<執筆の手順>
1. チャット履歴から顧客の基本情報（名前、年齢、性別、家族構成、悩み）を抽出
2. 顧客の深層心理と本当の悩みを推測
3. 顧客の言葉を引用して共感を示す導入部を書く
4. 第一章: 顧客が抱えている「傷」を3つ特定し、簡潔に描写（400字程度）
5. 第二章: 顧客の直感が正しいことを断言し、証拠を示す
6. 第三章: 顧客の悩みに関連する前世の物語を創作（宮廷の絵師、など具体的に）
7. 第四章: 未来のビジョンを「視えました」で始め、半年後、1年後、3年後を簡潔に描写（600字程度）
8. 第五章: 「三つの道」を簡潔に説明（400字程度）
9. 結び: 魂からの最後のメッセージで締めくくる

**最重要**: 合計文字数4,000～5,000字を厳守し、**5,000字を絶対に超えないでください。**各章は簡潔にまとめ、冗長な繰り返しや過度な詳細描写は避けてください。顧客の心に深く響く言葉を選び、共感と理解を示しつつ、全体のバランスを取って質の高い内容を凝縮してください。

**絶対に分割しないでください**: 「【続く】」「文字数制限のため分割」などの表現は絶対に使わないでください。導入部から結びまで、一度に全てを完成させてください。
</執筆の手順>

それでは、上記のルールを厳守して、深層鑑定【破】を一度に全て作成してください。`;

/**
 * 学習データから1件を選択してフォーマット（文体参考のみ）
 */
function formatTrainingExample(trainingData: any[]): string {
  // ランダムに1件選択
  const selected = trainingData[Math.floor(Math.random() * trainingData.length)];
  
  // 鑑定文の一部（最初の1,200文字）を抽出
  return selected.divinationText.substring(0, 1200) + '\n\n...（以下略）';
}

/**
 * 鑑定文を生成する
 */
export async function generateDivinationText(customerChatHistory: string): Promise<string> {
  // 学習データを取得
  const trainingData = await getAllTrainingData();
  
  if (trainingData.length === 0) {
    throw new Error('学習データが見つかりません');
  }
  
  // 学習データから1件を選択してフォーマット
  const trainingExample = formatTrainingExample(trainingData);
  
  // プロンプトを構築
  const prompt = DIVINATION_PROMPT_TEMPLATE
    .replace('{trainingExample}', trainingExample)
    .replace('{customerChatHistory}', customerChatHistory);
  
  // Claude Sonnet 4.5を呼び出し
  const messages = [
    {
      role: 'user' as const,
      content: prompt
    }
  ];
  
  const divinationText = await callClaudeSonnet(messages);
  
  return divinationText;
}
